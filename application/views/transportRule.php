								1. Bus facility will be provided on a 'first-come-first serve' basis according to the admission numbers.<br />
2. Parents should consult the school's transport in-charge for necessary details of bus routes or any changes desired.<br />
3. In case there is a permanent change in stop and/or bus, permission for the same has to be sought through a written application. The change shall become effective only after the school grants written permission. <br />
4. Parents should, ensure that their wards are always escorted to and from bus stops. For those children who for any reason, have to travel unescorted to and from their stops, an application mentioning the same must be sent to the Principal.<br />
5. A student using the school bus is expected to be at the bus stop at least five minutes before the scheduled arrival of the bus. The student has to be on the correct side of the arriving bus.<br />
6. Please do not enter school buses to see off or receive your wards.<br />
7. Please do not overtake or stop the school bus to facilitate the boarding of your ward as it endangers the safety of the children.<br />
8. Please do not get into an argument with the teachers, conductor or driver present in the bus. In case of problem, please give a written complaint to the school office .<br />
9. The drivers are authorized to stop buses at designated stops only, unless otherwise directed by the bus teacher after consultation with the I/C-Transport and Principal.<br />
10. Students are allowed to use only their allotted bus and bus stop. There is no provision for temporary change in buses or bus stop.<br />
11. Students, who miss the allotted bus, should not try to board any other bus. In such cases it is the responsibility of parents to drop their wards to the school. Such students may, however, return by their allotted bus.<br />
12. For their own safety, students are not allowed :<br />
<strong>.</strong> to go behind or under the buses parked or to sit in parked buses during school hours.<br />
			<strong>.</strong> to touch the instrument panel of the buses.<br />
			<strong>.</strong> to keep even their hands out for waving.<br />
			<strong>.</strong> to thrown any object inside or outside the bus.<br />
			<strong>.</strong> to shout in buses as it distracts the driver.<br />
13. Students must make sure school bags and other belongings are properly placed in the bus.<br />
14. The consumption of edibles and drinks is not permitted in the bus.<br />
15. Parents/Nominees of students who do not avail the school's transport should present themselves at the school gate 15 minutes before the start/ dispersal of the school to drop/collect their wards.
