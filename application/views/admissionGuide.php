								<h4><br />
  CLASSES NURSERY AND PREP OBSERVATION-CUM-INTERACTION</h4>
								
								<div class="sc_section bg_tint_none margin_bottom_1em_imp sc_features_st1" align="justify">
									
<p>Admission to classes Nursery and Prep will be granted on the basis of observation cum interaction.Presence of both the parents during the period is mandatory. Parents will have to bring the Admit and Identity cards, on the date and time mentioned on the cards, when they come for the observation-cum-interaction</strong>. The admit card will be handed over to the Teacher-in charge for observation cum interaction and the Identity Card will be retained by the parents to collect their children after the observation .</p>
<p>List of successful candidates will be posted on the notice board of the School and the School Website. The parents of these candidates will be required to contact the School office for the completion of the admission formalities.<br />
  <br />
</p>
<h4>CLASS I TO IX - WRITTEN TEST AND INTERACTION</h4>
<p>Class I onwards, a formal written test will be conducted in Language (English & Hindi) and Maths to check the linguistic proficiency ,mathematical ability for assessing the suitability for admission to the next class.<br />
  <br />
  For classes VI-IX there will be an additional test in Science. Parents will have to bring the Admit and Identity cards, on the date and time mentioned on the cards, when they come for the written test.
  <br />
  List of successful candidates will be posted on the notice board of the School and the School Website. The candidates who have cleared the written test will need to come to the School Administrative Office of the School for an interaction session along with both the parents.<br />
  <br />
</p>
<h4>DOCUMENTS REQUIRED FOR REGISTRATION</h4>
<ul>
  <ul>
    The following documents are to be deposited with the school:
  </ul>
</ul>
<br />
<ul>
  <li>1. The Registration form</li>
  <li>2. Admit and Identity Cards</li>
  <li>3. Photocopies of the following documents should be attached with the registration form :
    <ul>
      <li><strong> Birth certificate (Issued by Municipal Corporation or any competent authority. A certificate issued by doctors, nursing homes, hospitals etc will not be accepted)</li>
      <li><strong>The Last report card, for Class II onwards.</strong></li>
      <li><strong>Transfer certificate, for class II onwards.</strong></li>
    </ul>
  </li>
  <li>4. Originals of the above will be required for verification at the time of admission only. The registration form be summarily rejected, if there is any discrepancy found between the particulars filled in the form and the supporting documents.</li>
  <li>5. Paste three recent passport size colour photographs of the candidate (one of which should be pasted on the registration form, and the other two on the admit and identity cards.</li>
  <li>6. Enclose a coloured Passport size photograph of the parents along with the form.</li>
  <li>7. At the time of admission, a medical certificate from a registered medical practitioner and Indemnity bond supplied by the school will have to be furnished along with the other forms.</li>
</ul>
<h4>ADMISSION</h4>
<p>No candidate will be admitted to the school without clearing the observation-cum-interaction /written test and interaction. The parents of those candidates who have been selected for admission will be required to contact the School administrative office for the completion of admission formalities by depositing the required fee and other charges along with the necessary documents.</p>
